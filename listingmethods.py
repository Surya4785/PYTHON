#list.append

list = [2, 1, 3]
list.append(4)
print(list)

#list.sort

list = [2, 1, 3]
list.append(4)
list.sort()
print(list)

#list.sort(decending order)

list = [2, 1, 3]
list.append(4)
list.sort(reverse=True)
print(list)

#list.reverse

list = [2, 1, 3, 9, 7]
list.reverse()
print(list)

#list.insert

list = [2, 4, 7, 9]
list.insert(1,5)
print(list)

#list.remove and list.pop

list = [2, 1, 3, 1]
list.remove(1)
print(list)

list = [2, 1, 3, 1]
list.pop(2)
print(list)